CREATE OR REPLACE PROCEDURE ListVolunteersAndCountByProject
IS
/*
DESCRIPTION: Lists all volunteers by project and includes a count of
volunteers for each project.
Example: To run the query, please run the two lines below:
SET SERVEROUTPUT ON;
EXECUTE ListVolunteersAndCountByProject();
*/
BEGIN
