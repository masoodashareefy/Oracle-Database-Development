FOR rec IN (
SELECT ProjectID, ProjectName, Status,
CASE
WHEN Status = 'In Progress' THEN 'Active'
WHEN Status = 'Pending' THEN 'Pending'
ELSE 'Closed'
END AS StatusCategory
FROM Projects
) LOOP
DBMS_OUTPUT.PUT_LINE(RPAD(rec.ProjectID, 15) ||
RPAD(rec.ProjectName, 20) || rec.StatusCategory);
END LOOP;
END ListProjectStatusCategories;
