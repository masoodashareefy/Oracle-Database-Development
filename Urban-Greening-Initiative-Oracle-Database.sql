CREATE OR REPLACE PROCEDURE ListProjectStatusCategories
IS
/*
DESCRIPTION: lists all projects and categorizes their status into
'Active', 'Pending', or 'Closed'
Example: To run the query, please run the two lines below:
SET SERVEROUTPUT ON;
EXECUTE ListProjectStatusCategories;
*/
BEGIN
