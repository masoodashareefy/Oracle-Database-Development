-- Stored Procedure 6 Using Aggregate & Having
CREATE OR REPLACE PROCEDURE AverageCostPerProject
IS
/*
DESCRIPTION: Calculates the average cost per item for supplies used in
each project
and filters to include only those projects where the average cost
exceeds $500.
Example: To run the query, please run the two lines below:
SET SERVEROUTPUT ON;
EXECUTE AverageCostPerProject;
*/
v_project_id Projects.ProjectID%TYPE;
v_average_cost NUMBER;
BEGIN
DBMS_OUTPUT.PUT_LINE(RPAD('Project ID', 15) || 'Average Cost Per
Item');
FOR rec IN (
SELECT ProjectID, AVG(CostPerItem) AS AverageCostPerItem
FROM Supplies
GROUP BY ProjectID
HAVING AVG(CostPerItem) > 500 -- Only select projects with an
average cost > $500
) LOOP
v_project_id := rec.ProjectID;
v_average_cost := rec.AverageCostPerItem;
DBMS_OUTPUT.PUT_LINE(RPAD(v_project_id, 15) || v_average_cost);
END LOOP;
END AverageCostPerProject;
