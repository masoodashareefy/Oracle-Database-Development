-- Stored Procedure 3
CREATE OR REPLACE PROCEDURE ProjectDonationsAndSupplies IS
v_project_id Projects.ProjectID%TYPE;
v_data VARCHAR2(100);
/*
DESCRIPTION: Provides a financial summary for each project by listing the
total amount donated and the total cost of supplies.
Example: To run the query, please run the two lines below:
SET SERVEROUTPUT ON;
EXECUTE ProjectDonationsAndSupplies;
*/
BEGIN
DBMS_OUTPUT.PUT_LINE(RPAD('Project ID', 15) || RPAD('Type', 20) ||
'Amount');
FOR donation_rec IN (
SELECT ProjectID, 'Donations' AS DataType, SUM(AmountDonated) AS
TotalAmount
FROM Donors
GROUP BY ProjectID
UNION
SELECT ProjectID, 'Supplies Cost' AS DataType, SUM(CostPerItem *
QuantityAvailable) AS TotalAmount
FROM Supplies
GROUP BY ProjectID
) LOOP
v_project_id := donation_rec.ProjectID;
v_data := RPAD(donation_rec.DataType, 20) || donation_rec.TotalAmount;
DBMS_OUTPUT.PUT_LINE(RPAD(v_project_id, 15) || v_data);
END LOOP;
END ProjectDonationsAndSupplies;
