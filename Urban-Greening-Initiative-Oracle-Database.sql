CREATE OR REPLACE PROCEDURE GetSuppliesByProjectName
(
p_ProjectName IN Projects.ProjectName%TYPE
)
IS
/*
DESCRIPTION: Provides a list of items required for a specefic project and
their costs.
EXAMPLE: To run the query, please run the two lines below:
SET SERVEROUTPUT ON;
EXECUTE GetSuppliesByProjectName('ParkRestoration');
*/
BEGIN
DBMS_OUTPUT.PUT_LINE('Project Name' || ', ' || 'Supply Item' || ', '
|| 'Cost');
FOR rec IN (SELECT p.ProjectID, p.ProjectName, s.SupplyID, s.Item,
s.CostPerItem
FROM Projects p
JOIN Supplies s ON p.ProjectID = s.ProjectID
WHERE p.ProjectName = p_ProjectName) LOOP
DBMS_OUTPUT.PUT_LINE(rec.ProjectName || ', ' || rec.Item || ', '
|| rec.CostPerItem);
END LOOP;
END GetSuppliesByProjectName;
